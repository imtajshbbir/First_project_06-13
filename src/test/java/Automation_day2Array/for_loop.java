package Automation_day2Array;

public class for_loop
{
    public static void main(String[] args)
    {
        String[] states = new String[4];
        states [0] = "New york";
        states [1] = "California";
        states [2] = "ohio";
        states [3] = "Arizona";

        //defining the loop
        for (int i = 0; i <=3; i++){
            System.out.println("My state is " + states[i]);

        }

    }

}
